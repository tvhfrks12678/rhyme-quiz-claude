export function hiraganaToRomaji(text: string): string {
	let result = "";
	let i = 0;
	while (i < text.length) {
		const twoChar = text.slice(i, i + 2);
		if (ROMAJI_MAP[twoChar]) {
			result += ROMAJI_MAP[twoChar];
			i += 2;
		} else {
			const oneChar = text[i];
			result += ROMAJI_MAP[oneChar] ?? oneChar;
			i += 1;
		}
	}
	return result;
}
